
    var threeDotDiv = document.getElementById("threeDotDiv");
    function displayBlockDiv(){
        if(threeDotDiv.style.display === "block"){
            threeDotDiv.style.display = "none";
        }else{
            threeDotDiv.style.display = "block";
        }
    }


    // 
    var dropdowndiv = document.getElementById("dropdowndiv");
    function dropDownDiv(){
        if(dropdowndiv.style.display === "none"){
            dropdowndiv.style.display = "block"
        }else{
            dropdowndiv.style.display = "none"
        }
    }