        <h4 class="h4-about mt-4">
            Computer-delivered IELTS (CD IELTS)
        </h4>

        <p class="p-about">
            There are currently paper-based and computer-delivered versions of the IELTS exam available. A
            more recent variant called CD-IELTS, or computer-delivered IELTS, aims to make the test-taking
            process easier for pupils. Students can take the CD-IELTS exam on a computer by typing their
            answers to the several components, including Writing, Reading, and Listening. It is also a
            practical substitute for the paper exam, and students who are proficient with computers and have
            a fast typing speed can find it appealing. Additionally, in order to feel prepared and confident
            on test day, you must become familiar with the format and question types before to the test
        </p>
        <h4 class="h4-about ">
            IELTS format on a CD
        </h4>

        <p class="p-about">
            The format of the CD version of the IELTS is the same as the paper version. The CD-IELTS exam now has four divisions: Speaking,
            Writing, Reading, and Listening.
            <br><br>
            <b> 1. Listening: </b> Comprising four recorded audio sections with forty questions, the Listening portion of the CD IELTS test lasts about
            thirty minutes. Pupils are given recorded audio and asked to respond to questions depending on the spoken content of the audio.
            <br><br>
            <b> 2. Reading: </b> You will have 60 minutes to complete three reading passages with 40 questions in the Reading portion of the CD IELTS
            exam. Examinees must read the material and respond to questions using the information provided in the content.
            <br><br>
            <b> 3. Writing: </b> Students have 60 minutes to finish the writing portion of the assignment. This section has two tasks. Students' first
            objective is to write 150 words on graphs, charts, or diagrams. However, task 2 is based on broad subjects such as family, crime,
            environment, etc. Students must also prepare a 250-word essay.
            <br><br>
            <b> 4. Speaking: </b> Taking 11 to 14 minutes to finish, the Speaking portion of the CD IELTS test consists of a face-to-face interview with
            <br><br>
            a professional examiner. The speaking portion of the test must be taken by students in the same way as the paper-based IELTS
            speaking portion.
        </p>

        <h4 class="h4-about ">
            This is an explanation of the IELTS scoring methodology
        </h4>

        <p class="p-about">
            Students' accurate responses determine their actual scores for the reading and listening portions of the module. Pupils receive one <br>
            mark for every accurate response. There are no negative marking standards for incorrect answers, in contrast to other competitive
            exams. Students will ultimately receive scores ranging from 0 to 9 bands based on the overall number of accurate responses.
            <br><br>
            Writing and Speaking sections: The candidate's performance in a number of areas, including lexical resources, grammatical accuracy,
            coherence and cohesiveness, and pronunciation, determines how well they score in these sections. The pupils are then ultimately
            graded based on the average score they received across all four sections using the 0–9 band scale.
            <br><br>
            The test taker receives an IELTS Test Report Form (TRF) with the estimated scores, which displays the overall and individual band
            scores for each section. The TRF is utilized for work, study, and immigration purposes and is valid for two years after the test
            date.
            <br><br>
            In summary, the four areas of the CD IELTS assess a test taker's English language proficiency: speaking, reading, writing, and
            listening. Additionally, based on how well they perform in each segment, the students will receive a score in the 0–9 band range
        </p>

