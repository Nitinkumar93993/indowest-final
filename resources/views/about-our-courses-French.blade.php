        <h4 class="h4-about mt-4">
            What is GT Ielts
        </h4>

        <p class="p-about">
            In IELTS, there are four papers: Listening, Reading, Writing and Speaking. The Speaking and Listening tests are the same in both the Academic and the General Training tests, but the Reading and Writing tests are different.
        </p>

        <div class="ttm_single_image-wrapper text-center">
            <img style="border-radius: 25px; height: 700px; width: 100%;" class="img-fluid" src="/img/q1.webp" alt="single_10">
        </div>

        <h4 class="h4-about ">
            Listening
        </h4>

        <p class="p-about">
            Each of the four sections of the paper has ten questions. The answers to the first question will come before the answers to the second, and so on, because the questions are presented in the same order as the information in the recording.
            <br><br>
            The topics of Parts 1 and 2 are common social situations. In Part 1, there is a dialogue between two speakers (for instance, discussing trip plans). In Part 2, there is only one speaker (a lecture about local facilities, for example).
            <br><br>
            Training and instructional scenarios are covered in Parts 3 and 4. Part 3 is a dialogue between the two primary speakers, who could be two college students having a tutor-guided discussion. Only one speaker discusses an academic topic in Part 4.
            <br><br>
            The recordings will only be played to you once. There are various accents utilized, such as British, Australian, New Zealand, and North American.
            <br><br>
            You will have to copy your responses onto a sheet of paper. After the test, you will have ten minutes to complete this task. When filling out the answer sheet, you should take care because poor spelling and grammar will result in a mark deduction.
        </p>

        <h4 class="h4-about ">
            Summary
        </h4>

        <p class="p-about">
            <b> Time allowed: </b> Approximately 30 minutes (plus 10 minutes to transfer your answers to an answer sheet)
            <br><br>
            <b> Number of parts: </b> 4
            <br><br>
            <b> Number of questions: </b> 40
            <br><br>
            <b> Marking: </b> Each correct answer receives 1 mark. Your final score is given as a band score in whole or half bands, e.g. 5.5 or 7.0.

        </p>





        <h4 class="h4-about ">
            General Training Reading
        </h4>

        <p class="p-about">
            There are three sections of increasing difficulty.
            <br><br>
            The texts in Section 1 deal with everyday topics, and they are the sort of texts that a person would need to be able to understand when living in a country where English is spoken. You will need to pick out important information, e.g. from notices, advertisements and timetables.
            <br><br>
            The texts in Section 2 focus on work topics, for example, job descriptions, contracts, staff development and training materials.
            <br><br>
            The text in Section 3 deals with a topic of general interest. The style of writing in Section 3 is generally descriptive (containing detailed information) and instructive (telling you how to do something). The Section 3 text is longer and more complex than the texts in Sections 1 and 2. Section 3 texts are taken from newspapers, magazines, books and online resources.
            <br><br>
            You will need to transfer your answers to an answer sheet. You must transfer your answers during the hour you are given for the Reading test. Unlike the Listening test, no extra transfer time is given. You should be careful when writing your answers on the answer sheet because you will lose marks for incorrect spelling and grammar.
        </p>

        <h4 class="h4-about ">
            Summary
        </h4>

        <p class="p-about">
            <b> Time allowed: </b> 60 minutes (including transfer time)
            <br><br>
            <b> Number of sections: </b> 3; the total text length is 2150–2750 words
            <br><br>
            <b> Number of questions: </b> 40
            <br><br>
            <b> Marking: </b> Each correct answer receives 1 mark. Your final score is given as a band score from 1–9 in whole or half bands, e.g. 3 or 8.5.
        </p>

        <h4 class="h4-about ">
            General Training Writing
        </h4>

        <p class="p-about">
            There are two Writing tasks and BOTH must be completed.
            <br><br>
            In Task 1, you have to respond to a situation by writing a letter, for example, asking for information or explaining a situation. You need to write at least 150 words in about 20 minutes.
            <br><br>
            In Task 2, you are given a point of view, argument or problem which you need to discuss. You need to write at least 250 words in about 40 minutes.
            <br><br>
            You must write your answers using full sentences. You must not write your answers as notes or bullet points. You must write your answers on the answer sheet. You are allowed to write notes on the question paper but these will not be seen by the examiner.
        </p>




        <h4 class="h4-about ">
            Summary
        </h4>


        <p class="p-about">
            Time allowed: 60 minutes
            <br>
            Number of tasks: 2
            <br>
            Marking: Task 2 contributes twice as much as Task 1 to the Writing score.
        </p>
        <h4 class="h4-about ">
            Speaking
        </h4>


        <p class="p-about">
            The Speaking test is a face-to-face interview between the test taker and an examiner. The Speaking test is recorded.
            <br><br>
            There are three parts to the test, and each part follows a specific pattern of tasks in order to test your speaking ability in different ways.
            <br><br>
            In Part 1 you will be asked about yourself and about familiar topics, such as home and family, studies or interests.
            <br><br>
            In Part 2 you are shown a card asking you to talk about a particular topic. After a minute to prepare, you must speak for up to two minutes and then answer general questions on the topic.
            <br><br>
            In Part 3 further questions will be asked about the topic in more detail.
        </p>

        <h4 class="h4-about ">
            Summary
        </h4>


        <p class="p-about">
            Time allowed: 11–14 minutes

            Number of parts: 3
        </p>







