        <h4 class="h4-about ">
            IELTS modules
        </h4>

        <p class="p-about">
            Students considering taking the IELTS exam must be aware of the four modules of the IELTS exam. Listening and speaking modules are
            the same for IELTS academic and IELTS general. The only difference lies in the reading and writing module format. A good IELTS
            coaching center always gives students an idea about the difference between the two types. So, Let's discuss them in-depth today
        </p>





        <p class="p-about">
            <b>1. Listening</b>
        </p>

        <ul class="ani-ul">

            <li>The listening module is a task of 30 minutes, with an extra 10 minutes provided to transfer the answer to the answer sheet.
            </li>
            <li>Four recordings are provided to measure the listening skills of a student. Recording1 includes a two-person conversation based on
                the everyday social situation, Recording2 has a monologue on social context, Recording3 contains a conversation among four persons </li>
            <li>For each video, ten questions are asked, each with one mark for the correct answer. The total number of questions asked is 40</li>
            <li>In order to help students discover the answer at the moment they heard the material, questions are posed in the order that the
                videos play. Videos may only be shown to students once. Thus, pay close attention to what is said. A variety of conversational
                accents are used to assess the students' command of the English language</li>
            <li>The module includes multiple-choice questions (MCQs), matching, labeling plans, maps, and diagrams, form, note, table, flowchart,
                summary completion, and sentence completion.</li>
            <li>Marking criteria are based on the number of correct answers. A student can get a maximum of 40 marks and a minimum of 0. According
                to the obtained marks, bands are given to the students through the band conversion table below</li>
        </ul>


        <p class="p-about">
            <b>2(i). Reading(Academic)</b>
        </p>
        <p class="p-about">
            This module is considered the toughest, so students always choose a trusted IELTS coaching center to be good at the module. Find out
            the element of the reading module that puts students in trouble.

        </p>

        <ul class="ani-ul">

            <li>The module takes 60 minutes to end. And 40 questions are asked to calculate the students' English reading skills. No extra time is
                granted for the answer transmission.
            </li>
            <li>Three texts range from descriptive and date-oriented to discursive and systematic. General books, newspapers, magazines, and
                journals are the sources of the texts. The paragraphs are general readings that are written for non-technical aspirants</li>
            <li>Multiple choice questions (MCQs), information retrieval, identifying the author's opinions or assertions, information matching,
                heading matching, feature matching, sentence ending matching, sentence completion, summary completion, note completion, table
                completion, flowchart completion, diagram-label completion, and short questions-answers are some examples of the question types</li>
            <li>Every question is worth one mark, and pupils receive one mark for a right response. An incorrect answer to a question carries no
                penalty points.</li>
            <li>The amount of right answers is equal to the reading score of a student. The band conversion table is then used to convert the score
                to bands, as explained below</li>
        </ul>

