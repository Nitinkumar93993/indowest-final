            <b> 2 (ii). Perusing (All Categories) </b>
        </p>


        <ul class="ani-ul">
            <li>Between the IELTS general and academic versions, there are just three variations. The texts included in the modules are sourced
                from manuals, notices, pamphlets, brochures, newspapers, and periodicals, which is the first distinction.
            </li>
            <li>Content on incidents, work, interests, and daily life can be found in all three volumes</li>
            <li>Below is the band conversion table for this module.
            </li>
        </ul>


        <p class="p-about">
            <b> 3(i). Writing(Academic) </b>
        </p>


        <ul class="ani-ul">
            <li>The writing module demands students' command over the language. Students looking to improve their writing skills must enroll in a
                good IELTS coaching center familiar with the module's below-mentioned specifics.
            </li>
            <li>There are two assignments to complete in the 60-minute IELTS writing module</li>
            <li>In Task 1, test takers are asked to describe, in their own words, visual information presented in the form of facts and figures,
                which may include a table, graph, diagram, or chart. Students will have 20 minutes to convey the topic in 150 words or less.
            </li>
            <li>Writing an essay on the provided argument, problem, or point of view is task number two. The writing style is wholly unique.
                Students are given a word limit of 250 and forty minutes to complete their work</li>
            <li>The four factors used to compute a student's score are</li>
            <li>Task accomplishment: This assesses the task 1 response and gauges how appropriately, accurately, and properly the provided
                statistics are translated into writing form.</li>
            <li>Task Response: It assesses task 2 and looks at how test-takers put the provided statement together. Examples from personal
                experience and proofs should be included in the writing to bolster the written information. Write within the allotted word limit;
                less than 250 words will be penalized.
            </li>
            <li>Coherence and cohesion: This criterion assesses the written material's fluency and clarity as well as its organization and the
                degree to which it connects concepts, language, and information. Cohesion is the skillful use of logical conjunctions, pronouns,
                and connectors. Coherence is the process of connecting concepts through logical sequencing.
            </li>
            <li>Lexical resource: This resource looks at how widely words are used in writing in relation to the circumstances.</li>
            <li>Grammatical correctness and range: This indicates that the test requires the ability to apply grammar effectively in writing</li>

        </ul>
        <p class="p-about">
            <b> 3(ii). Writing(General) </b>
        </p>


        <ul class="ani-ul">
            <li>There are only two differences between the IELTS academic and IELTS general. First, task 1 is to write a letter explaining the
                condition according to the situation presented to the students. The letter can be in a semi-formal, formal, or personal writing
                style. The word limit for the letter is 150. Students need to complete the task in 20 minutes</li>
            <li>The second one is about the task achievement parameter. It evaluates the task 1 response and measures how suitably, precisely, and
                aptly the written letter meets the set requirements within the limit of 150 words</li>

        </ul>
        <p class="p-about">
            <b>4. Expressing </b>
        </p>


        <ul class="ani-ul">
            <li>The ability to communicate well helps students receive high exam grades. Participating in speaking exercises is encouraged for
                students attending a professional IELTS coaching center. Learn about the specifics of this module and the metrics used for
                evaluation</li>
            <li>The purpose of this activity is to assess the pupils' speaking abilities. It is split up into three sections. This module lasts for
                eleven to fourteen minutes.
            </li>
            <li> In Part 1Students' personal information was gathered during the Part 1 Introduction and Interview. In the same section, the
                examiner introduces himself and verifies the pupils' identities. Personal information includes inquiries about well-known subjects
                including name, hometown, employment, education, and interests. This section takes four to five minutes to finish.

            </li>
            <li>In Part 2, often known as the lengthy turn portion, test takers discuss a specific subject from the cue card provided by the
                examiner. You have one minute to be ready for the speech by taking some rough notes with a pen and paper. Test takers get one to
                two minutes to speak. This takes three to four minutes to complete.</li>
            <li>Part 3, the discussion, involves a short conversation on the cue-card topic between a test taker and examiner for 4-5 minutes.
                Students need to justify the given reasons and opinions in their speech.
            </li>
        </ul>

